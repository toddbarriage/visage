def main():
    grab = parse_cur(CUR_DIR / "hand_grab.cur")
    grabbing = parse_cur(CUR_DIR / "hand_grabbing.cur")

    parts = [
        LICENSE_HEADER,
        "",
        "namespace visage {",
        "namespace cursor_data {",
        "",
        "struct EmbeddedCursor {",
        "  int width;",
        "  int height;",
        "  int hotspot_x;",
        "  int hotspot_y;",
        "  int bits_per_pixel;",
        "  const unsigned char* color_bits;",
        "  int color_size;",
        "  const unsigned char* and_mask;",
        "  int mask_size;",
        "};",
        "",
        "// hand_grab (open hand)",
        emit_array("kGrabColorBits", grab["color"]),
        "",
        emit_array("kGrabAndMask", grab["mask"]),
        "",
        "inline constexpr EmbeddedCursor kGrab = {",
        f"  {grab['width']}, {grab['height']},  // width, height",
        f"  {grab['hotspot_x']}, {grab['hotspot_y']},  // hotspot",
        f"  {grab['bpp']},  // bits_per_pixel",
        f"  kGrabColorBits, sizeof(kGrabColorBits),",
        f"  kGrabAndMask, sizeof(kGrabAndMask),",
        "};",
        "",
        "// hand_grabbing (closed fist)",
        emit_array("kGrabbingColorBits", grabbing["color"]),
        "",
        emit_array("kGrabbingAndMask", grabbing["mask"]),
        "",
        "inline constexpr EmbeddedCursor kGrabbing = {",
        f"  {grabbing['width']}, {grabbing['height']},  // width, height",
        f"  {grabbing['hotspot_x']}, {grabbing['hotspot_y']},  // hotspot",
        f"  {grabbing['bpp']},  // bits_per_pixel",
        f"  kGrabbingColorBits, sizeof(kGrabbingColorBits),",
        f"  kGrabbingAndMask, sizeof(kGrabbingAndMask),",
        "};",
        "",
        "}  // namespace cursor_data",
        "}  // namespace visage",
        "",
    ]

    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUT_PATH.write_text("\n".join(parts))

    print(f"Wrote {OUT_PATH}")
    for name, c in [("kGrab", grab), ("kGrabbing", grabbing)]:
        print(
            f"  {name}: {c['width']}x{c['height']} @{c['bpp']}bpp, "
            f"hotspot=({c['hotspot_x']},{c['hotspot_y']})"
        )
        print(f"    color: {len(c['color'])} bytes, mask: {len(c['mask'])} bytes")


if __name__ == "__main__":
    main()
