#!/usr/bin/env python3
"""Extract cursor bitmap data from Chromium .cur files into a C++ header.

Parses the 24-bit image from each .cur file (hand_grab.cur, hand_grabbing.cur)
and writes cursor_data.h in the Win32 windowing directory, with pre-extracted
pixel arrays + hotspots + BSD-3 attribution.

Usage (from libs/visage/tools/):
    python3 extract_cursor.py

Re-run if the source .cur files are updated.
"""

import struct
from pathlib import Path

HERE = Path(__file__).parent
CUR_DIR = HERE / "cursors"
OUT_PATH = HERE.parent / "visage_windowing" / "win32" / "cursor_data.h"


def parse_cur(path):
    """Parse a .cur file and return the 24-bit 32x32 image + hotspot + masks."""
    data = path.read_bytes()
    _, ctype, count = struct.unpack_from("<HHH", data, 0)
    if ctype != 2:
        raise RuntimeError(f"{path}: not a CUR file (type={ctype})")

    for i in range(count):
        entry_off = 6 + 16 * i
        w, h, _pal, _res, hx, hy, _size, off = struct.unpack_from(
            "<BBBBHHII", data, entry_off
        )
        w = 256 if w == 0 else w
        h = 256 if h == 0 else h
        bpp = struct.unpack_from("<H", data, off + 14)[0]
        if bpp == 24 and w == 32 and h == 32:
            bih_size = struct.unpack_from("<I", data, off)[0]
            image_start = off + bih_size
            row_bytes = ((w * bpp + 31) // 32) * 4
            color_size = row_bytes * h
            mask_row = ((w + 31) // 32) * 4
            mask_size = mask_row * h
            return {
                "width": w,
                "height": h,
                "hotspot_x": hx,
                "hotspot_y": hy,
                "bpp": bpp,
                "color": data[image_start : image_start + color_size],
                "mask": data[image_start + color_size : image_start + color_size + mask_size],
            }

    raise RuntimeError(f"{path}: no 24-bit 32x32 image found")


def emit_array(name, data):
    rows = []
    for i in range(0, len(data), 12):
        chunk = data[i : i + 12]
        rows.append("    " + ", ".join(f"0x{b:02x}" for b in chunk) + ",")
    return (
        f"static const unsigned char {name}[{len(data)}] = {{\n"
        + "\n".join(rows)
        + "\n};"
    )


